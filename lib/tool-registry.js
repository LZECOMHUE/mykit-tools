// === Helpers ===

export function getToolBySlug(slug) {
  return tools.find((t) => t.slug === slug) || null;
}

export function getToolsByCategory(category) {
  return tools.filter(
    (t) => t.category === category || (t.alsoIn && t.alsoIn.includes(category))
  );
}

export function getToolsByTag(tag) {
  return tools.filter((t) => t.tags.includes(tag));
}

export function getRelatedTools(slug, limit = 6) {
  const tool = getToolBySlug(slug);
  if (!tool) return [];

  // If manually specified, use those
  if (tool.relatedSlugs) {
    return tool.relatedSlugs
      .map(getToolBySlug)
      .filter(Boolean)
      .slice(0, limit);
  }

  // Auto-match: same category first, then overlapping tags
  const candidates = tools
    .filter((t) => t.slug !== slug)
    .map((t) => {
      let score = 0;
      if (t.category === tool.category) score += 3;
      // Boost if cross-listed in same category
      if (t.alsoIn && t.alsoIn.includes(tool.category)) score += 2;
      if (tool.alsoIn && tool.alsoIn.includes(t.category)) score += 2;
      const overlap = t.tags.filter((tag) => tool.tags.includes(tag)).length;
      score += overlap;
      return { ...t, _score: score };
    })
    .sort((a, b) => b._score - a._score);

  return candidates.slice(0, limit);
}

export function getAllSlugs() {
  return tools.map((t) => t.slug);
}

export function getToolCount() {
  return tools.length;
}

export function getCategoryCounts() {
  const counts = {};
  for (const tool of tools) {
    counts[tool.category] = (counts[tool.category] || 0) + 1;
  }
  return counts;
}

// Tags that are too generic to be useful as filters
const EXCLUDED_FILTER_TAGS = new Set([
  "calculator", "converter", "generator", "uk", "tool", "free", "online",
  "printable", "game", "quiz", "metric", "imperial",
]);

/**
 * Get the most useful filter tags for a category, sorted by frequency.
 * Excludes overly generic tags that don't help users narrow down results.
 */
export function getCategoryFilterTags(category, maxTags = 12) {
  const categoryTools = getToolsByCategory(category);
  const tagCounts = {};
  for (const tool of categoryTools) {
    for (const tag of tool.tags) {
      if (!EXCLUDED_FILTER_TAGS.has(tag)) {
        tagCounts[tag] = (tagCounts[tag] || 0) + 1;
      }
    }
  }
  // Only show tags that appear on more than one tool (useful for filtering)
  // unless the category is small (fewer than 6 tools)
  const minCount = categoryTools.length < 6 ? 1 : 2;
  return Object.entries(tagCounts)
    .filter(([, count]) => count >= minCount)
    .sort((a, b) => b[1] - a[1])
    .slice(0, maxTags)
    .map(([tag]) => tag);
}

/**
 * Get featured tools for a category (tier 1 first, then tier 2, up to limit).
 */
export function getFeaturedTools(category, limit = 6) {
  const categoryTools = getToolsByCategory(category);
  const sorted = [...categoryTools].sort((a, b) => {
    // Tier 1 first, then tier 2, then tier 3
    if (a.tier !== b.tier) return a.tier - b.tier;
    // Within same tier, newest first
    return (b.dateAdded || "").localeCompare(a.dateAdded || "");
  });
  return sorted.slice(0, limit);
}
