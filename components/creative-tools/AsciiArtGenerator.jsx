const FONTS = [
  { label: "Banner", value: "banner", data: BANNER },
  { label: "Standard", value: "standard", data: STANDARD },
  { label: "Small", value: "small", data: SMALL },
  { label: "Shadow", value: "shadow", data: SHADOW },
];

function renderAscii(text, fontData, maxWidth) {
  const upper = text.toUpperCase();
  const height = Object.values(fontData)[0]?.length || 5;

  // Build all characters
  const charBlocks = [];
  for (const ch of upper) {
    const block = fontData[ch];
    if (block) {
      charBlocks.push(block);
    } else {
      // Unknown char - render as space
      const spaceBlock = fontData[" "];
      if (spaceBlock) charBlocks.push(spaceBlock);
    }
  }

  if (charBlocks.length === 0) return "";

  // Combine with 1 space between characters
  const lines = [];
  for (let row = 0; row < height; row++) {
    let line = charBlocks.map((b) => (b[row] || "")).join(" ");
    if (line.length > maxWidth) line = line.slice(0, maxWidth);
    lines.push(line);
  }
  return lines.join("\n");
}

export default function AsciiArtGenerator() {
  const [text, setText] = useState("HELLO");
  const [font, setFont] = useState("banner");
  const [maxWidth, setMaxWidth] = useState(80);
  const [copied, setCopied] = useState(false);
  const outputRef = useRef(null);

  const fontData = FONTS.find((f) => f.value === font)?.data || BANNER;
  const ascii = useMemo(() => renderAscii(text, fontData, maxWidth), [text, fontData, maxWidth]);

  const handleCopy = () => {
    navigator.clipboard.writeText(ascii).catch(() => {});
    setCopied(true);
    setTimeout(() => setCopied(false), 1200);
  };

  return (
    <div className="space-y-4">
      {/* Text input */}
      <input
        type="text"
        value={text}
        onChange={(e) => setText(e.target.value)}
        placeholder="Type something..."
        className="w-full border border-border rounded-xl px-4 py-3 text-lg focus:outline-none focus:border-accent"
        maxLength={30}
      />

      {/* Controls */}
      <div className="flex flex-wrap items-center gap-3">
        <div className="flex gap-1">
          {FONTS.map((f) => (
            <button
              key={f.value}
              onClick={() => setFont(f.value)}
              className={`px-3 py-1.5 rounded-full text-xs font-medium transition-colors ${
                font === f.value
                  ? "bg-accent text-white"
                  : "bg-surface text-text-secondary hover:bg-surface-hover"
              }`}
            >
              {f.label}
            </button>
          ))}
        </div>
        <div className="flex-1 min-w-[140px] max-w-[240px]">
          <Slider label="Width" value={maxWidth} onChange={setMaxWidth} min={40} max={120} />
        </div>
        <Button size="sm" onClick={handleCopy} disabled={!ascii}>
          {copied ? "Copied!" : "Copy"}
        </Button>
      </div>

      {/* Output */}
      <div className="overflow-x-auto rounded-xl border border-border bg-[#1a1a1a] p-4">
        <pre
          ref={outputRef}
          className="font-mono text-xs sm:text-sm text-green-400 leading-tight whitespace-pre"
        >
          {ascii || "Start typing above..."}
        </pre>
      </div>
    </div>
  );
}
